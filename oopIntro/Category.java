package oopIntro;

public class Category {
    private int id;
    private String name;

    public Category(int id, java.lang.String name) {
        this.id = id;
        this.name = name;
    }

    public int getId() {
        return this.id;
    }
    public void setId(int id) {
        this.id = id;
    }
    public java.lang.String getName() {
        return this.name;
    }
    public void setName(java.lang.String name) {
        this.name = name;
    }


}
