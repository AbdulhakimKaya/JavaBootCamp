package HomeworkDay2;

public class Course {

    int id;
    String courseName;
    String teacherName;
    double unitPrice;

    public Course(int id, String courseName, String teacherName, double unitPrice) {
        this.id = id;
        this.courseName = courseName;
        this.teacherName = teacherName;
        this.unitPrice = unitPrice;
    }
}
